#include <stdio.h>

int main() {
	int i = 1;
    
    // Using swith statement
	switch (i) {
	case 1:
		printf("Case 1");
		break;
	case 2:
		printf("Case 2");
		break;
	case 3:
		printf("Case 3");
		break;
	case 4:
		printf("Case 4");
		break;
	default:
		printf("Default");
		break;
	}
  return 0;
}
